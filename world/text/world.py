import world.obstacles as obstacle

# variables tracking position and direction
position_x = 0
position_y = 0
directions = ['forward', 'right', 'back', 'left']
current_direction_index = 0

# area limit vars
min_y, max_y = -180, 180
min_x, max_x = -80, 80


def show_position(robot_name):
    """
    This function will show the current position of the robot.
    :param robot_name:
    """
    print(' > '+robot_name+' now at position ('+str(position_x)+','+str(position_y)+').')


def is_position_allowed(new_x, new_y):
    """
    Checks if the new position will still fall within the max area limit
    :param new_x: the new/proposed x position
    :param new_y: the new/proposed y position
    :return: True if allowed, i.e. it falls in the allowed area, else False
    """

    return min_x <= new_x <= max_x and min_y <= new_y <= max_y


def update_position(steps):
    """
    Update the current x and y positions given the current direction, and specific nr of steps
    :param steps:
    :return: True if the position was updated, else False
    """

    global position_x, position_y, new_x, new_y
    new_x = position_x
    new_y = position_y

    if directions[current_direction_index] == 'forward':
        new_y = new_y + int(steps)
    elif directions[current_direction_index] == 'right':
        new_x = new_x + int(steps)
    elif directions[current_direction_index] == 'back':
        new_y = new_y - int(steps)
    elif directions[current_direction_index] == 'left':
        new_x = new_x - int(steps)

    if is_position_allowed(new_x, new_y):
        position_x = new_x
        position_y = new_y
        return True
    return False


def do_forward(robot_name, steps):
    """
    Moves the robot forward the number of steps
    :param robot_name:
    :param steps:
    :return: (True, forward output text)
    """
    if update_position(steps):
        return True, ' > '+robot_name+' moved forward by '+str(steps)+' steps.'
    if obstacle.is_position_blocked(new_x, new_y):
        return True , f"{robot_name} Sorry, there is an obstacle in the way."
    else:
        return True, ''+robot_name+': Sorry, I cannot go outside my safe zone.'


def do_back(robot_name, steps):
    """
    Moves the robot forward the number of steps
    :param robot_name:
    :param steps:
    :return: (True, forward output text)
    """

    if update_position(-steps):
        return True, ' > '+robot_name+' moved back by '+str(steps)+' steps.'
    if obstacle.is_position_blocked(new_x, new_y):
        return True , f"{robot_name} Sorry, there is an obstacle in the way."
    else:
        return True, ''+robot_name+': Sorry, I cannot go outside my safe zone.'


